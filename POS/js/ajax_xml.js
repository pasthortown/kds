$(document).ready(function(){
	fn_cargarFactura();
});

////////////////////////////////CARGAR FACTURA//////////////////////////////////////////////
function fn_cargarFactura() {
	send={"datosFactura":1};
	send.fctId=$("#txtFacturaId");
	
	$.ajax({
		async:false,type:"GET",dataType: "json",contentType: "application/x-www-form-urlencoded",url:"config_xml.php",data:send,success:function(datos) {
			if(datos.str>0) {
				for(i=0; i<datos.str; i++) {
         			$("#txtFacturaList").append("<option value="+datos[i]['fpf_id']+">"+datos[i]['cfac_id']+"</option>");
				}
       		}
		}
	});  
}


